            <h3>About Qingdao</h3>
            <p>Qingdao (also spelled Tsingtao) is a city in eastern Shandong Province on the east coast of China. Qīng (青) in Chinese means "cyan" or "greenish-blue", while dǎo (岛) means "island". Qingdao is a unique city; its uniqueness lies in its cultural significance, colonial history, and strategic importance in Modern China. Mount Lao, northeastern to Downtown Qingdao, is regarded as the cradle of Taosim; Qufu City, eastern to Qingdao, is the hometown of Confucius. At the end of the 19th century, Qingdao became a colony of Germany and the continuing occupation by Japan after 1914 stimulated one of the most significant youth movements the May Fourth Movement in 1919. Current Qingdao is a major seaport, naval base, and industrial center. It is one of the easter hubs for President Xi’s One Road One Belt Strategy.

            </p>

