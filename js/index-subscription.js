$(document).ready(function(){
    //This is to control the appearance and disappearance of the subscription page
    $("#myNav").hide();
    $("#crosssign").click(function(){
        $("#myNav").fadeOut(200);
        $("#noti").text("");

    });
    $("#header-sb").click(function(){
        $("#myNav").fadeIn(200);
        $("#noti").text("");
    })




}); 