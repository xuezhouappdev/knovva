                <p>Social Innovation and Entrepreneurship is a year-round program designed to help 9th - 12th graders from around the world develop a deeper understanding of international social issues and students’ own capacities to make meaningful impact. SIE has three modules in the winter, spring and summer.
                    During the winter module, students will spend a week in Nairobi, Kenya for a culturally immersive experience and case study, taking field trips with local experts to learn about the social and environmental reality of the region. After the trip, online courses will be offered introducing social entrepreneurship models in a global setting. In the three-week summer module in Cambridge MA, students will participate in a practicum to develop their own initiative to address a social or environmental problem of their interest. Students will form teams, study a pressing challenge, and begin the process of program launch.
